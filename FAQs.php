    <p>
        <ul>
          <strong>1. Check refund status:</strong>
            <p>The fund is under guaranteed. Please check the order details to confirm the refund process under following conditions: order is closed/cancelled after payment, dispute is finished with refund.</p>
                <ol>1) If the refund is successfully processed, you will receive the refund within 3-20 business days, please contact your bank (provide the ARN number if there is) to follow up the Refund status.</ol>
                <ol>2) If the refund is under processing, which means refund has been issued by AliExpress successfully, we will also speed up the process for you, you can also contact your bank for detailed information.</ol>
